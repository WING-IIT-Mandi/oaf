<p><b><a name="Faq_4"></a>4. When I paste text from a word processor into the rich text fields of the application, the system changes the formatting of my document. Is there a way to avoid this? </b></p>
<p>When completing an application component that contains a rich text field, you will notice a formatting toolbar. The farthest right button on the toolbar is a clipboard image with the letter W. Click this button, and a secondary window will appear. (You may need to modify your browser settings to enable pop-ups.) Paste the text directly from your word processor into this field, and click &ldquo;insert.&rdquo;</p>

<p>This should allow you to insert your text with minimal disruption to the formatting. We do recommend, however, that you double-check your application before submitting it.</p>
<p><a href="#top">Back to Top</a></p>

<p><b><a name="5"></a>5. Why do I get a character error message when I paste information into a restricted character fields?</b></p>
<p>When pasting text into the Application form fields from Word files, extraneous background formatting will sometimes be counted as characters resulting in an error message.&nbsp; If pasting information, you should stay well below the character limit for the field.</p>
