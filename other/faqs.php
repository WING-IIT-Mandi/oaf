<p><b><a name="Faq_3"></a>3. I did not receive the Activation email as indicated at the end of the Online Application Wizard registration process. How can I get my registration activated?</b><p>
<p>There are a number of reasons related to your Internet Service Provider (ISP) and/or email account provider that may result in loss of email correspondence from the Administration, notably:
<br/>(a)Email account quota is full.
<br/>(b)Email is directed to your Junk/Spam folder.
<br/>(c)Email with attachments are blocked by your service provider.
<br/>Please check with your ISP/email account provider. Otherwise, consider opening a free "Gmail" or "Yahoo" email account.

